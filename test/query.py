# 2. LANGCHAIN SQL AGENT SETUP
# ============================================================================

class SQLAgent:
    """
    LangChain SQL Agent with full execution pipeline
    Ensures queries are not just generated but EXECUTED
    """
    
    def __init__(self, 
                 db: SQLDatabase,
                 model: str = "llama3.2:1b",
                 temperature: float = 0,
                 verbose: bool = True):
        """
        Initialize SQL Agent
        
        Args:
            db: SQLDatabase connection object
            model: Ollama model (llama3.2:3b recommended, not 1b)
            temperature: LLM temperature (0 = deterministic)
            verbose: Show agent reasoning steps
        """
        self.db = db
        self.model_name = model
        self.temperature = temperature
        self.verbose = verbose
        self.agent_executor = None
        self.llm = None
    
    def setup(self) -> AgentExecutor:
        """
        Setup LLM and SQL Agent Executor
        
        Returns:
            Configured AgentExecutor (this is what EXECUTES queries)
        """
        try:
            logger.info(f"Initializing LLM: {self.model_name}")
            
            # Initialize LLM
            self.llm = ChatOllama(
                model=self.model_name,
                temperature=self.temperature,
                num_predict=512,  # Max output tokens
                top_p=0.95,
                top_k=40
            )
            logger.info("✅ LLM initialized")
            
            # Create SYSTEM PROMPT for better accuracy
            system_prompt = """You are an expert SQL database assistant.
            
CRITICAL RULES:
1. ALWAYS generate VALID PostgreSQL queries
2. NEVER explain, ONLY execute
3. Use table aliases for clarity
4. Always filter unnecessary rows
5. Use aggregate functions when appropriate
6. Format results clearly
7. Handle NULL values appropriately

When given a question, ALWAYS use the available tools to:
1. First, understand the table structure
2. Then, write the SQL query
3. Finally, execute it and return results

Respond ONLY with the query results, no explanations."""
            
            # Create SQL Agent Executor
            # THIS is what actually EXECUTES the queries!
            self.agent_executor = create_sql_agent(
                llm=self.llm,
                db=self.db,
                agent_type="tool-calling",  # Important: must be tool-calling
                verbose=self.verbose,
                system_prompt=system_prompt,
                handle_parsing_errors=True,  # Graceful error handling
                max_iterations=5  # Prevent infinite loops
            )
            
            logger.info("✅ SQL Agent Executor created and ready")
            logger.info(f"Verbose mode: {self.verbose}")
            
            return self.agent_executor
        
        except Exception as e:
            logger.error(f"Failed to setup agent: {e}")
            raise
    
    def query(self, user_input: str) -> Dict[str, Any]:
        """
        Execute a natural language query
        
        This is the COMPLETE PIPELINE:
        User Input → LLM generates SQL → Executor runs SQL → Database returns results → 
        LLM formats answer → Natural Language output
        
        Args:
            user_input: Natural language question
        
        Returns:
            Dictionary with output and intermediate steps
        """
        if not self.agent_executor:
            raise RuntimeError("Agent not initialized. Call setup() first.")
        
        try:
            logger.info(f"\n{'='*70}")
            logger.info(f"User Query: {user_input}")
            logger.info(f"{'='*70}")
            
            # INVOKE THE AGENT
            # This triggers the full pipeline:
            # 1. LLM reads user input
            # 2. LLM generates SQL (tool call)
            # 3. Executor runs the SQL on PostgreSQL
            # 4. Results come back
            # 5. LLM formats natural language response
            response = self.agent_executor.invoke(
                {"input": user_input},
                return_only_outputs=False  # See all intermediate steps
            )
            
            logger.info(f"\n{'='*70}")
            logger.info("FINAL ANSWER:")
            logger.info(f"{'='*70}")
            logger.info(response.get("output", "No output"))
            
            return response
        
        except Exception as e:
            logger.error(f"Query execution failed: {e}")
            logger.error("This usually means:")
            logger.error("  1. SQL syntax error in generated query")
            logger.error("  2. Table/column name mismatch")
            logger.error("  3. Database connection issue")
            logger.error("  4. LLM hallucinated a non-existent table")
            raise

