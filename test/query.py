class PostgreSQLConnection:
    """Handle PostgreSQL connection with proper error handling"""
    
    def __init__(self, 
                 host: str = "localhost",
                 port: int = 5432,
                 user: str = "postgres",
                 password: str = "postgres",
                 database: str = "postgres"):
        """
        Initialize PostgreSQL connection
        
        Args:
            host: Database host (default: localhost)
            port: Database port (default: 5432)
            user: Database user
            password: Database password
            database: Database name
        """
        self.host = host
        self.port = port
        self.user = user
        self.password = password
        self.database = database
        self.connection_string = None
        self.db = None
    
    def build_connection_string(self) -> str:
        """Build PostgreSQL connection string"""
        self.connection_string = (
            f"postgresql://{self.user}:{self.password}@"
            f"{self.host}:{self.port}/{self.database}"
        )
        return self.connection_string
    
    def connect(self, table_names: Optional[list] = None) -> SQLDatabase:
        """
        Connect to PostgreSQL database
        
        Args:
            table_names: List of specific tables to include (optional)
        
        Returns:
            SQLDatabase object for LangChain
        """
        try:
            self.build_connection_string()
            logger.info(f"Connecting to PostgreSQL: {self.host}:{self.port}/{self.database}")
            
            # Create SQLDatabase connection
            if table_names:
                self.db = SQLDatabase.from_uri(
                    self.connection_string,
                    include_tables=table_names
                )
                logger.info(f"Connected to tables: {table_names}")
            else:
                self.db = SQLDatabase.from_uri(self.connection_string)
                logger.info("Connected to database (all tables available)")
            
            # Test connection
            self._test_connection()
            return self.db
        
        except Exception as e:
            logger.error(f"Failed to connect to PostgreSQL: {e}")
            raise
    
    def _test_connection(self):
        """Test database connection by running simple query"""
        try:
            result = self.db.run("SELECT 1")
            logger.info("✅ Database connection test passed")
        except Exception as e:
            logger.error(f"❌ Database connection test failed: {e}")
            raise
    
    def get_tables(self) -> list:
        """Get list of all available tables"""
        try:
            tables = self.db.get_usable_table_names()
            logger.info(f"Available tables: {tables}")
            return tables
        except Exception as e:
            logger.error(f"Failed to get table list: {e}")
            return []
    
    def get_schema(self, table_name: str) -> str:
        """Get schema information for a table"""
        try:
            schema = self.db.get_table_info([table_name])
            logger.info(f"Schema for {table_name}:\n{schema}")
            return schema
        except Exception as e:
            logger.error(f"Failed to get schema for {table_name}: {e}")
            return ""

