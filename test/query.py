def execute_sql_directly(db: SQLDatabase, sql_query: str) -> pd.DataFrame:
    """
    Execute SQL directly without going through LLM
    Useful for debugging or when LLM fails
    
    Args:
        db: SQLDatabase connection
        sql_query: Raw SQL query
    
    Returns:
        Results as pandas DataFrame
    """
    try:
        logger.info(f"Executing SQL directly: {sql_query}")
        result = db.run(sql_query)
        logger.info("Query executed successfully")
        return result
    except Exception as e:
        logger.error(f"SQL execution failed: {e}")
        raise

